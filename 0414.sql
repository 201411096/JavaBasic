CREATE TABLE CUSTOMER(
    CUSTOMER_ID NUMBER(4) UNIQUE,
    CUSTOMER_NAME VARCHAR2(10) NOT NULL,
    CUSTOMER_TEL VARCHAR2(20),
    CUSTOMER_PASSWORD VARCHAR2(20),
    CUSTOMER_PASSWORD_CHECK VARCHAR2(20),
    CUSTOMER_REMAINED_MONEY NUMBER(10) NOT NULL,
    CUSTOMER_ADDRESS VARCHAR2(40),
    CUSTOMER_IS_MEMBER VARCHAR2(1),
    CUSTOMER_AGE NUMBER(3),
    CONSTRAINT CUSTOMER_TEL_PK PRIMARY KEY(CUSTOMER_TEL)
);

