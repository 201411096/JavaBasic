package b_operator;

/*
 * 이진논리 &(and) |(or) ^(xor)
 * */

public class Ex07_BinaryLogical {

	public static void main(String[] args) {
		
		int a=15; //      1111
		int b = 10; //    1010
		System.out.println(a&b); // 1010
		System.out.println(a|b); // 1111
		System.out.println(a^b); // 0101

	}

}
